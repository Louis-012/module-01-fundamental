// Variable: data container

/**
 * how to define variable => variableKeyword variableName = data
 
 * variable keywords: var, let, const
 * variable name : must start with letter or _ or $
 *                 must not seperated by space
 * data: data type that can be defined: string, number, boolean, undefined, array, object
 */

// Defining variable
var name;

// Initializing variable
var age = 18;

// Variables keywords
// var : redeclare & reassign
// redeclare
var address = "Jl. Pemuda Surabaya";
var address = "Sukolilo Surabaya";

// reassign
address = "Rungkut Surabaya";

// let : non-redeclare, reassignable
let program = "Web Development";
// let program = "Digital Marketing"; //cannot redeclare

// const : non-redeclare & non-reassign
const Pi = 3.14;
const URLDomain = "https://google.com";
